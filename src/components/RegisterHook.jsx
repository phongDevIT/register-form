import React from "react";
import { useForm } from "react-hook-form";
import CheckBoxHook from "./checkbox/CheckBoxHook";
import DropDownHook from "./dropdown/DropDownHook";
import InputHook from "./input/InputHook";
import RadioHook from "./radio/RadioHook";
import { yupResolver } from "@hookform/resolvers/yup";
import * as yup from "yup";

const schema = yup
    .object({
        username: yup.string().required("Please enter your username"),
        email: yup
            .string()
            .email("Please enter valid email address")
            .required("Please enter your email address"),
        password: yup
            .string()
            .min(8, "Your password must be at least 8 characters or greater")
            // .matches(
            //   /^(?=.*[a-z])(?=.*[A-Z])(?=.*\d)(?=.*[@$!%*?&])[A-Za-z\d@$!%*?&]{8,}$/,
            //   {
            //     message:
            //       "Your password must have at least 1 uppercase, 1 lowercase, 1 special character",
            //   }
            // )
            .required("Please enter your password"),
        gender: yup
            .string()
            .required("Please select your gender")
            .oneOf(["male", "female"], "You can only select male or female"),
        job: yup
            .string()
            .required("Please select your job")
            .oneOf(["teacher", "developer", "doctor", "constructor"]),
        term: yup.boolean().required("Please accept the terms and conditions"),
    })
    .required();
const dropdownData = [
    {
        id: 1,
        value: "teacher",
        text: "Teacher",
    },
    {
        id: 2,
        value: "developer",
        text: "Developer",
    },
    {
        id: 3,
        value: "doctor",
        text: "Doctor",
    },
    {
        id: 4,
        value: "constructor",
        text: "Constructor",
    },
];

const RegisterHook = () => {
    const {
        handleSubmit,
        formState: { errors, isValid, isSubmitting, isSubmitSuccessful },
        control,
        setValue,
        getValues,
        reset,
        watch,
    } = useForm({
        resolver: yupResolver(schema),
        mode: "onChange",
        defaultValues: {
            gender: "male",
        },
    });
    console.log(errors);
    // console.log("RegisterHook ~ isSubmitting", isSubmitting);
    // console.log("RegisterHook ~ errors", errors);
    const onSubmitHandler = (values) => {
        if (!isValid) return;
        return new Promise((resolve) => {
            setTimeout(() => {
                resolve();
                reset({
                    username: "",
                    email: "",
                    password: "",
                    gender: "male",
                    job: "",
                    term: false,
                });
            }, 5000);
        });
    };
    const watchGender = watch("gender");
    console.log("Rendering React hook form");
    return (
        <form
            onSubmit={handleSubmit(onSubmitHandler)}
            className="max-w-[300px] mx-auto my-10"
            autoComplete="off"
        >
            <div className="flex flex-col gap-3 mb-5">
                <label htmlFor="username" className="cursor-pointer">
                    Username
                </label>
                <InputHook
                    name="username"
                    placeholder="Enter your username"
                    id="username"
                    control={control}
                    type="text"
                ></InputHook>
                {errors.username && (
                    <p className="text-sm text-red-500">
                        {errors.username.message}
                    </p>
                )}
            </div>
            <div className="flex flex-col gap-3 mb-5">
                <label htmlFor="email" className="cursor-pointer">
                    Email address
                </label>
                <InputHook
                    name="email"
                    placeholder="Enter your email"
                    id="email"
                    control={control}
                    type="email"
                ></InputHook>
                {errors.email && (
                    <p className="text-sm text-red-500">
                        {errors.email.message}
                    </p>
                )}
            </div>
            <div className="flex flex-col gap-3 mb-5">
                <label htmlFor="password" className="cursor-pointer">
                    Password
                </label>
                <InputHook
                    name="password"
                    placeholder="Enter your password"
                    id="password"
                    control={control}
                    type="password"
                ></InputHook>
                {errors.password && (
                    <p className="text-sm text-red-500">
                        {errors.password.message}
                    </p>
                )}
            </div>
            <div className="flex flex-col gap-3 mb-5">
                <label className="cursor-pointer">Gender</label>
                <div className="flex items-center gap-5">
                    <div className="flex items-center gap-x-3">
                        <RadioHook
                            control={control}
                            name="gender"
                            value="male"
                            checked={watchGender === "male"}
                        ></RadioHook>
                        <span>Male</span>
                    </div>
                    <div className="flex items-center gap-x-3">
                        <RadioHook
                            control={control}
                            name="gender"
                            value="female"
                            checked={watchGender === "female"}
                        ></RadioHook>
                        <span>Female</span>
                    </div>
                </div>
                {errors.gender && (
                    <p className="text-sm text-red-500">
                        {errors.gender.message}
                    </p>
                )}
            </div>
            <div className="flex flex-col gap-3 mb-5">
                <label className="cursor-pointer">Are You</label>
                <DropDownHook
                    control={control}
                    setValue={setValue}
                    name="job"
                    data={dropdownData}
                ></DropDownHook>
            </div>
            <div className="">
                <CheckBoxHook
                    control={control}
                    name="terms"
                    text="I accept the term and conditions"
                ></CheckBoxHook>
            </div>
            <button className="w-full bg-blue-500 p-5 text-white rounded-lg font-semibold">
                Submit
            </button>
        </form>
    );
};

export default RegisterHook;
